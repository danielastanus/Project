public class Calculator {



        public double sum (int x, int y) {
            double result = x + y;
            return result;
        }


        public  double substraction (int x, int y){
            double result1 = x - y;
            return result1;
        }

        public double multiplication (int x, int y){
            double result2 = x * y;
            return result2;
        }

        public float division (float x, float y){
            float result3 = x/y;
            return result3;
        }
        public int modulus (int x, int y){
            int result4 = x%y;
            return result4;
        }



}
